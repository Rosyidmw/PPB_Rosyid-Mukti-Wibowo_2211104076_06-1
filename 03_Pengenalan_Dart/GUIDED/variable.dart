void main() { 
// Menggunakan var 
  var name = "Rosyid Mukti Wibowo"; // Tipe data String 
  var age = 20; // Tipe data Integer 
  print("Nama: $name, Usia: $age"); 
  
  // Type Annotation
  String nama = "Rosyid";
  int umur = 20;

  print('Nama : $nama, Umur : $umur');

  // Multiple variable 
  String firstName, lastName; // Tipe data String 
  firstName = "Charlie"; 
  lastName = "Brown"; 
  print("Nama Lengkap: $firstName $lastName");
} 